<?php
/**
 * Category Grid Block - Server-Side Render.
 *
 * @package WBCOM_Essential
 *
 * @var array    $attributes Block attributes.
 * @var string   $content    Block default content.
 * @var WP_Block $block      Block instance.
 */

if ( ! defined( 'ABSPATH' ) ) {
	exit;
}

// Extract attributes with defaults.
$use_theme_colors    = ! empty( $attributes['useThemeColors'] );
$columns             = isset( $attributes['columns'] ) ? absint( $attributes['columns'] ) : 4;
$columns_tablet      = isset( $attributes['columnsTablet'] ) ? absint( $attributes['columnsTablet'] ) : 2;
$columns_mobile      = isset( $attributes['columnsMobile'] ) ? absint( $attributes['columnsMobile'] ) : 1;
$selected_categories = isset( $attributes['selectedCategories'] ) ? array_map( 'absint', (array) $attributes['selectedCategories'] ) : array();
$exclude_empty       = isset( $attributes['excludeEmpty'] ) ? (bool) $attributes['excludeEmpty'] : true;
$max_categories      = isset( $attributes['maxCategories'] ) ? absint( $attributes['maxCategories'] ) : 8;
$show_post_count     = isset( $attributes['showPostCount'] ) ? (bool) $attributes['showPostCount'] : true;
$show_image          = isset( $attributes['showImage'] ) ? (bool) $attributes['showImage'] : true;
$image_ratio         = isset( $attributes['imageRatio'] ) ? sanitize_text_field( $attributes['imageRatio'] ) : '0.75';
$gap                 = isset( $attributes['gap'] ) ? absint( $attributes['gap'] ) : 24;
$card_border_radius  = isset( $attributes['cardBorderRadius'] ) ? absint( $attributes['cardBorderRadius'] ) : 12;
$card_bg_color       = isset( $attributes['cardBgColor'] ) ? sanitize_hex_color( $attributes['cardBgColor'] ) : '';
$name_color          = isset( $attributes['nameColor'] ) ? sanitize_hex_color( $attributes['nameColor'] ) : '';
$count_color         = isset( $attributes['countColor'] ) ? sanitize_hex_color( $attributes['countColor'] ) : '';
$overlay_color       = isset( $attributes['overlayColor'] ) ? sanitize_hex_color( $attributes['overlayColor'] ) : '';
$order_by            = isset( $attributes['orderBy'] ) ? sanitize_text_field( $attributes['orderBy'] ) : 'name';
$sort_order          = isset( $attributes['order'] ) ? sanitize_text_field( $attributes['order'] ) : 'ASC';

// Build get_categories args.
$cat_args = array(
	'orderby'    => $order_by,
	'order'      => $sort_order,
	'number'     => $max_categories,
	'hide_empty' => $exclude_empty,
);

if ( ! empty( $selected_categories ) ) {
	$cat_args['include'] = $selected_categories;
}

$categories = get_categories( $cat_args );

if ( empty( $categories ) || is_wp_error( $categories ) ) {
	echo '<p class="wbcom-essential-category-grid__empty">' . esc_html__( 'No categories found.', 'wbcom-essential' ) . '</p>';
	return;
}

// Build CSS custom properties.
$style_vars = array(
	'--cat-columns: ' . $columns,
	'--cat-columns-tablet: ' . $columns_tablet,
	'--cat-columns-mobile: ' . $columns_mobile,
	'--cat-gap: ' . $gap . 'px',
	'--cat-border-radius: ' . $card_border_radius . 'px',
	'--cat-image-ratio: ' . $image_ratio,
);

if ( ! $use_theme_colors ) {
	if ( $card_bg_color ) {
		$style_vars[] = '--cat-card-bg: ' . $card_bg_color;
	}
	if ( $name_color ) {
		$style_vars[] = '--cat-name-color: ' . $name_color;
	}
	if ( $count_color ) {
		$style_vars[] = '--cat-count-color: ' . $count_color;
	}
	if ( $overlay_color ) {
		$style_vars[] = '--cat-overlay-color: ' . $overlay_color;
	}
}

$classes = array( 'wbcom-essential-category-grid' );
if ( $use_theme_colors ) {
	$classes[] = 'use-theme-colors';
}

$wrapper_attributes = get_block_wrapper_attributes(
	array(
		'class' => implode( ' ', $classes ),
		'style' => implode( '; ', $style_vars ),
	)
);

?>
<div <?php echo $wrapper_attributes; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Generated by get_block_wrapper_attributes(). ?>>
	<div class="wbcom-essential-category-grid__items" role="list">
		<?php foreach ( $categories as $category ) : ?>
			<?php
			$cat_link     = esc_url( get_category_link( $category->term_id ) );
			$cat_name     = esc_html( $category->name );
			$cat_count    = absint( $category->count );
			$thumbnail_id = get_term_meta( $category->term_id, 'thumbnail_id', true );
			$has_image    = $show_image && ! empty( $thumbnail_id );

			// Generate a consistent hue from category ID for placeholder.
			$placeholder_hue = ( $category->term_id * 137 ) % 360;
			$first_letter    = mb_strtoupper( mb_substr( $category->name, 0, 1, 'UTF-8' ), 'UTF-8' );
			?>
			<a href="<?php echo $cat_link; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Already escaped. ?>"
				class="wbcom-essential-category-grid__card<?php echo $has_image ? ' has-image' : ' has-placeholder'; ?>"
				role="listitem"
				aria-label="<?php echo esc_attr( $cat_name ); ?>">

				<div class="wbcom-essential-category-grid__media">
					<?php if ( $has_image ) : ?>
						<?php echo wp_get_attachment_image( $thumbnail_id, 'medium_large', false, array( 'class' => 'wbcom-essential-category-grid__image' ) ); ?>
					<?php elseif ( $show_image ) : ?>
						<div class="wbcom-essential-category-grid__placeholder"
							style="--cat-placeholder-hue: <?php echo esc_attr( $placeholder_hue ); ?>"
							aria-hidden="true">
							<span class="wbcom-essential-category-grid__letter"><?php echo esc_html( $first_letter ); ?></span>
						</div>
					<?php endif; ?>
					<div class="wbcom-essential-category-grid__overlay" aria-hidden="true"></div>
				</div>

				<div class="wbcom-essential-category-grid__content">
					<h3 class="wbcom-essential-category-grid__name"><?php echo $cat_name; // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Already escaped. ?></h3>
					<?php if ( $show_post_count ) : ?>
						<span class="wbcom-essential-category-grid__count">
							<?php
							printf(
								/* translators: %s: number of posts */
								esc_html( _n( '%s Post', '%s Posts', $cat_count, 'wbcom-essential' ) ),
								esc_html( number_format_i18n( $cat_count ) )
							);
							?>
						</span>
					<?php endif; ?>
				</div>
			</a>
		<?php endforeach; ?>
	</div>
</div>
